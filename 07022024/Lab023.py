# Literals

# var_name = var_value
# var_name -> Identifier
# var_value -> Literal

# Literals are actually values assigned
# Literals can be numeric / non-numeric in nature
name = "Pramod"  # String literal
age = 12  # int literal
pi = 3.14  # float literal
char = 'C'
print(type(char))  # String literal
