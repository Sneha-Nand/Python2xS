# pass and break in the python
# in pass that no will be skipped
for i in range(1,11):
    if(i==5):
        pass
    else:
        print(i)

# in break that whole loop will be break when no is equal to 5
print("+++++++++++++++++++++++++++++++++++++")
for i in range(1,11):
    if(i==5):
        break
    else:
        print(i)